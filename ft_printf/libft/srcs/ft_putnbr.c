#include "libft.h"

void	ft_putnbr(int nb)
{
	int t;

	t = 0;
	if (nb < 0)
	{
		if (nb == -2147483648)
			t = 1;
		ft_putchar('-');
		nb = -nb - t;
	}
	if (nb >= 10)
		ft_putnbr(nb / 10);
	ft_putchar(nb % 10 + '0' + t);
}
