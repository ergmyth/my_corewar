#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	t;

	t = 0;
	if (n < 0)
	{
		if (n == -2147483648)
			t = 1;
		ft_putchar_fd('-', fd);
		n = -n - t;
	}
	if (n >= 10)
		ft_putnbr_fd(n / 10, fd);
	ft_putchar_fd(n % 10 + '0' + t, fd);
}
